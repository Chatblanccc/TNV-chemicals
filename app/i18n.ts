export function t(locale: Locale, english: string): string {
  return locale === "zh" ? zh[english] ?? english : english;
}

export function splitLocalizedRoute(input: string): { locale: Locale; route: string } {
  const match = input.match(/^\/(en|zh)(\/.*)?$/);
  if (!match) return { locale: "en", route: input || "/" };
  return { locale: match[1] as Locale, route: match[2] || "/" };
}

export function localizedPath(locale: Locale, route: string): string {
  return route === "/" ? `/${locale}` : `/${locale}${route}`;
}

export function alternateLocale(locale: Locale): Locale {
  return locale === "en" ? "zh" : "en";
}
